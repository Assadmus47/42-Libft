#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	s;

	s = 0;
	sign = 1;
	i = 0;
	while ((str[i] >= 7 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		s = (10 * s) + (str[i] - '0');
		i++;
	}
	return (s * sign);
}
